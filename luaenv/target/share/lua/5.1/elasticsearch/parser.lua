-------------------------------------------------------------------------------
-- Importing module
-------------------------------------------------------------------------------
local cjson = require "cjson.safe"

-------------------------------------------------------------------------------
-- Declaring module
-------------------------------------------------------------------------------
local parser = {}

-------------------------------------------------------------------------------
-- Encodes a table to a json string
-------------------------------------------------------------------------------
parser.jsonEncode = cjson.encode

-------------------------------------------------------------------------------
-- Decodes a json string to a table
-------------------------------------------------------------------------------
parser.jsonDecode = cjson.decode

return parser
